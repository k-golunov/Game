    class Box
    {
        Texture2D Image;
        Rectangle Rectangle;
        public int NumberTexture;

        public Box(Texture2D image, Rectangle rect, int numberTexture)
        {
            Image = image;
            Rectangle = rect;
            NumberTexture = numberTexture;
        }

        public void Draw(SpriteBatch spriteBatch)
        {
            spriteBatch.Draw(Image, Rectangle, Color.White);
        }
