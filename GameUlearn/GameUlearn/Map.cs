        public void GenerateMap()
        {
            var x = 40;
            var y = 20;
            

            for (var i = 0; i < map.GetLength(0); i++)
            { 
                for (var j = 0; j < map.GetLength(1); j++)
			    {
                    var rect = new Rectangle(x, y, 64, 64);
                    boxs.Add(new Box(Image[map[i,j]], rect, map[i, j]));
                    x += 64;
			    }
                y += 64;
            }
        }
    }

